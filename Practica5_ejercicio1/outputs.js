/*
*Simple console output
*@param {string} - Mensaje
*@returns {alert} Muestra el mensaje por consola  
*/
function console_output(msg) {
    console.log(msg)
}

/*
*Simple alert output
*@param {string} - Mensaje 
*@returns {alert} Muestra el mensaje con un Alert
*/
function alert_output(msg) {
    alert(msg)
}